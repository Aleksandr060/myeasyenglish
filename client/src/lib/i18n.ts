type TranslationKey = keyof typeof translations.en;

export const t = (
  language: Language,
  key: TranslationKey,
  params?: Record<string, string | number>
) => {
  const template = String(translations[language][key] || translations.en[key]);

  if (!params) return template;

  return Object.entries(params).reduce<string>(
    (result, [name, value]) => result.replace(`{${name}}`, String(value)),
    template
  );
};

export const getQuestionTypeLabel = (type: Question["type"], language: Language) => {
  const labels = {
    en: {
      vocabulary: "Vocabulary Quiz",
      translation: "Translation Drill",
      listening: "Listening",
      fillblank: "Fill in the Blank",
      speaking: "Speaking"
    },
    ru: {
      vocabulary: "Словарный тест",
      translation: "Перевод",
      listening: "Аудирование",
      fillblank: "Заполни пропуск",
      speaking: "Говорение"
    }
  } as const;

  return labels[language][type];
};

export const formatLocalizedDate = (value: string | null, language: Language) => {
  if (!value) return t(language, "justNow");
  return new Date(value).toLocaleDateString(language === "ru" ? "ru-RU" : "en-US", {
    day: "numeric",
    month: "short"
  });
};
